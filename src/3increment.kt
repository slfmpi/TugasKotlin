fun main() {
    var runtutan = '1'

    // Pre-incrementing
    println("Nomor " + ++runtutan)
    println("Nomor " + ++runtutan)
    println("Nomor " + ++runtutan)
    println("")

    // Post-incrementing
    println("Nomor " + runtutan++)
    println("Nomor " + runtutan++)
    println("Nomor " + runtutan++)
    println("")

    // Pre-incrementing
    println("Nomor " + --runtutan)
    println("Nomor " + --runtutan)
    println("Nomor " + --runtutan)
    println("")

    // Post-decrementing
    println("Nomor " + runtutan--)
    println("Nomor " + runtutan--)
    println("Nomor " + runtutan--)
}
